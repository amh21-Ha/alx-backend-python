#!/usr/bin/env python3
"""
Module to define a function for adding two floats.
"""


def add(a: float, b: float) -> float:
    """
    Add two floats and return their sum.

    Args:
        a (float): The first float to add.
        b (float): The second float to add.

    Returns:
        float: The sum of a and b.
    """
    return a + b
